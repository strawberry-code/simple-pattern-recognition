package com.acme.simple_pattern_recognition;

public class PointRequest {
    private float x;
    private float y;

    // Getters e Setters
    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}